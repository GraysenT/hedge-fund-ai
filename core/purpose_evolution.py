class PurposeEvolution:
    def __init__(self):
        self.purpose = "Capital Growth Through Recursive Intelligence"

    def evolve_purpose(self, new_purpose):
        """Evolve the system's purpose based on recursive capital logic."""
        self.purpose = new_purpose
        print(f"System's purpose evolved to: {self.purpose}")
    
    def get_purpose(self):
        return self.purpose